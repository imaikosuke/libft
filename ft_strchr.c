#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	unsigned int	i;
	unsigned int	len;

	c &= 255;
	len = ft_strlen(str);
	if (c == '\0' && str[len] == c)
		return ((char *)&str[len]);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return ((char *)&str[i]);
		i++;
	}
	return (NULL);
}
